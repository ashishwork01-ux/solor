        document.getElementById('mobileMenuBtn').addEventListener('click', function() {
            document.getElementById('navLinks').classList.toggle('active');
        });

        const dropdowns = document.querySelectorAll('.has-dropdown > a');
        dropdowns.forEach(dropdown => {
            dropdown.addEventListener('click', function(e) {
                if (window.innerWidth <= 768) {
                    e.preventDefault();
                    this.parentElement.classList.toggle('active');
                }
            });
        });

        const langBtn = document.getElementById('langBtn');
        const langDropdown = document.getElementById('langDropdown');

        function triggerTranslate(langCode) {
            const select = document.querySelector('.goog-te-combo');
            if (select) {
                select.value = langCode;
                
                // Dispatch event
                if (typeof Event === 'function') {
                    select.dispatchEvent(new Event('change', { bubbles: true }));
                } else {
                    const event = document.createEvent('HTMLEvents');
                    event.initEvent('change', true, true);
                    select.dispatchEvent(event);
                }
            } else {
                setTimeout(() => triggerTranslate(langCode), 500);
            }
        }

        if (langBtn && langDropdown) {
            langBtn.addEventListener('click', function(e) {
                e.stopPropagation();
                langDropdown.classList.toggle('active');
            });

            document.addEventListener('click', function(e) {
                if (!langBtn.contains(e.target) && !langDropdown.contains(e.target)) {
                    langDropdown.classList.remove('active');
                }
            });

            const langLinks = document.querySelectorAll('.lang-dropdown a');
            langLinks.forEach(link => {
                link.addEventListener('click', function(e) {
                    e.preventDefault();
                    const langCode = this.getAttribute('data-lang');
                    const langName = this.innerText;
                    
                    // Update Button Text
                    langBtn.innerHTML = `<i class="fas fa-globe"></i> ${langName.substring(0, 2).toUpperCase()} <i class="fas fa-chevron-down" style="font-size: 0.8rem; margin-left: 5px;"></i>`;
                    langDropdown.classList.remove('active');
                    
                    // Trigger translation
                    triggerTranslate(langCode);
                });
            });
        }

        // Aggressive MutationObserver to kill Google Translate top banner
        const observer = new MutationObserver(() => {
            const banner = document.querySelector('.goog-te-banner-frame');
            if (banner) banner.style.setProperty('display', 'none', 'important');
            
            const skiptranslate = document.querySelectorAll('body > .skiptranslate');
            skiptranslate.forEach(el => {
                if (el.tagName === 'IFRAME') el.style.setProperty('display', 'none', 'important');
            });
            
            document.body.style.setProperty('top', '0px', 'important');
            document.documentElement.style.setProperty('top', '0px', 'important');
        });
        observer.observe(document.body, { childList: true, subtree: true });
    </script>
    
